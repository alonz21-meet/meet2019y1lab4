import turtle
turtle.bgcolor('purple')
turtle.pensize(5)
turtle.color('yellow')
turtle.shape('circle')
t2=turtle.clone()
t2.color('green')
t2.shape('triangle')
t2.pensize(10)
t2.goto(0,-150)
t2.goto(75,-150)
t2.color('blue')
t2.goto(75,0)
turtle.goto(0,150)
turtle.goto(-75,150)
turtle.color('white')
turtle.goto(-75,0)
t2.left(90)
turtle.mainloop()
             
