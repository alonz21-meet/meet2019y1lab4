import turtle
turtle.shape('turtle')
finn=turtle.clone()
finn.shape('square')
finn.goto(100,100)
finn.goto(0,100)
finn.goto(0,200)
finn.goto(100,200)
finn.goto(100,100)
charlie=turtle.clone()
charlie.shape('triangle')
charlie.goto(100,100)
charlie.goto(200,0)
charlie.goto(0,0)
finn.penup()
finn.goto(-100,100)
finn.stamp()
finn.goto(300,300)
charlie.penup()
turtle.penup()
charlie.goto(300,270)
turtle.goto(300,240)
finn.pendown()
charlie.pendown()
turtle.pendown()
finn.clearstamps()
turtle.mainloop
